CSymbol::CSymbol( EStyle style, QBrush brush, QPen pen, QSize size )
{
    m_style = style;
    m_brush = brush;
    m_pen = pen;
    m_size = size;
}

/////////////////////////////////////////////////////////////

QwtSymbol* CSymbol::ToQwtSymbol()
{
    return new QwtSymbol( QwtSymbol::Style( m_style ), m_brush, m_pen, m_size );
}
